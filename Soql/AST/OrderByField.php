<?php
namespace Codemitte\ForceToolkit\Soql\AST;

class OrderByField extends AbstractOrderByField
{
    /**
     * @var string
     */
    private $name;

    /**
     * @param string $name
     */
    public function __construct($name)
    {
        $this->name = $name;
    }

    /**
     * @return string
     */
    public function getName()
    {
        return $this->name;
    }
}
